const slika = document.querySelector('.slider .opisISlika .slika img');
const opis = document.querySelector('.slider .opisISlika .opis');
const imeSlike = opis.querySelector('#ime');
const godina = opis.querySelector('#godina');
const opisSlike = opis.querySelector('#opiss');
const tacke = document.querySelectorAll('.dots .dot');

let index = 0;

function showSlide(n) {
    if (n >= slike.length) { index = 0; }
    if (n < 0) { index = slike.length - 1; }

    slika.src = slike[index].src;
    imeSlike.innerHTML = slike[index].name;
    godina.innerHTML = slike[index].god;
    opisSlike.innerHTML = slike[index].desc;

    for (let i = 0; i < tacke.length; i++) {
        tacke[i].classList.remove('active');
    }

    tacke[index].classList.add('active');

    slika.classList.remove('show');
    void slika.offsetWidth; 
    slika.classList.add('show');
}

function nextSlide() {
    index++;
    if (index >= slike.length) {
        index = 0;
    }
    showSlide(index);
}

function currentSlide(n) {
    index = n;
    showSlide(index);
}

window.onload = function () {
    showSlide(index);
    setInterval(nextSlide, 3000); 
};
